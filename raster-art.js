    // ─── Compositing ────────────────────────────────────────

    function compositeMap() {
        const grid = Array.from({length: GRID_ROWS}, () => Array(GRID_COLS).fill(null));

        for (const [part, forms] of Object.entries(PART_FORMS)) {
            const idx = part === 'nest' ? 0 : PARTS[part].current;
            for (const [r, c] of forms[idx]) {
                grid[r][c] = part;
            }
        }
        return grid;
    }

    // ─── Rendering ──────────────────────────────────────────

    const SVG_DOT = '<svg viewBox="0 0 20 20"><circle cx="10" cy="10" r="8"/></svg>';

    function renderGrid(docked) {
        const map = docked ? compositeMap() : BIRD_MAP_UNDOCKED;
        let html = '<div class="raster-grid">';

        for (let row = 0; row < GRID_ROWS; row++) {
            html += '<div class="raster-row">';
            for (let col = 0; col < GRID_COLS; col++) {
                const part = map[row][col];
                if (part === null) {
                    html += '<span class="dot empty"></span>';
                } else if (part === 'nest') {
                    html += `<span class="dot nest-part" data-part="nest">${SVG_DOT}</span>`;
                } else {
                    html += `<span class="dot bird-part" data-part="${part}">${SVG_DOT}</span>`;
                }
            }
            html += '</div>';
        }

        html += '</div>';
        return html;
    }

    // ─── Public API ─────────────────────────────────────────

    function cyclePart(partName) {
        const part = PARTS[partName];
        if (!part) return -1;
        part.current = (part.current + 1) % NUM_FORMS;
        return part.current;
    }

    function getPartState(partName) {
        return PARTS[partName] ? PARTS[partName].current : 0;
    }

    function getPartLabel(partName) {
        const idx = PARTS[partName] ? PARTS[partName].current : 0;
        const names = SHAPE_NAMES[partName];
        return names ? names[idx] : '';
    }

    function render(docked) {
        return renderGrid(docked);
    }

